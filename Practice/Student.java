
package Practice;

public class Student {
    String name;
    int id;
    int age;
    String department;
    double gpa;

    // Constructor
    Student(String name, int id, int age, String department, double gpa) {
        this.name = name;
        this.id = id;
        this.age = age;
        this.department = department;
        this.gpa = gpa;
    }
}
