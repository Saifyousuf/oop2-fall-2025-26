package Practice;

public class Animal {
    String type;
    String name;
    int age;
    double weight;
    String habitat;

    // Constructor
    Animal(String type, String name, int age, double weight, String habitat) {
        this.type = type;
        this.name = name;
        this.age = age;
        this.weight = weight;
        this.habitat = habitat;
    }
}
