package Practice;

public class Teacher {
    String name;
    int teacherId;
    String subject;
    int experience;
    double salary;

    // Constructor
    Teacher(String name, int teacherId, String subject, int experience, double salary) {
        this.name = name;
        this.teacherId = teacherId;
        this.subject = subject;
        this.experience = experience;
        this.salary = salary;
    }
}
