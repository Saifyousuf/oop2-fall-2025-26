package Practice;

public class Car {
    String brand;
    String model;
    int year;
    String color;
    double engineCapacity;

    // Constructor
    Car(String brand, String model, int year, String color, double engineCapacity) {
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.engineCapacity = engineCapacity;
    }
}
